s = 'Python 基礎必修課'
#0->p, 1->y, 2->t, 3->h, 4->o...
#-12->p, -11->y, -10->t, -9->h, -8->o...
print (s[3])
print (s[-2])
#print (s1[16])   16超出，會報錯

print (s[:])
print (s[7:])
print (s[:6])#取到所指位置，但不包含其位置的值
print (s[5:8])
print (s[9:6:-1])
print (s[::2])#間隔兩個字，跳著取
print (s[::-1])#-代表逆向取值
print (s[::-2])#-代表逆向取值，間隔兩個字
